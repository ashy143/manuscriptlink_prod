          <h3 class="pageTitle">A note on fair use</h3>
            <p>One of the rights accorded to the owner of copyright is the right to reproduce or to authorize others to reproduce the work in copies or phonorecords. This right is subject to certain limitations found in sections 107 through 118 of the copyright law (<a href="/title17">title 17, U. S. Code</a>). One of the more important limitations is the doctrine of &ldquo;fair use.&rdquo; The doctrine of fair use has developed through a substantial number of court decisions over the years and has been codified in section 107 of the copyright law.</p>
            <p> Section 107 contains a list of the various purposes for which the reproduction of a particular work may be considered fair, such as criticism, comment, news reporting, teaching, scholarship, and research. Section 107 also sets out four factors to be considered in determining whether or not a particular use is fair.</p>
              <ol class="fair">
                <li>The purpose and character of the use, including whether such use is of commercial nature or is for nonprofit educational purposes</li>
                <li> The nature of the copyrighted work</li>
                <li> The amount and substantiality of the portion used in relation to the copyrighted work as a whole</li>
                <li> The effect of the use upon the potential market for, or value of, the copyrighted work</li>
              </ol>
            <p>The distinction between what is fair use and what is infringement in a  particular case will not always be clear or easily defined. There is no  specific number of words, lines, or notes that may safely be taken without  permission. Acknowledging the source of the copyrighted material does not  substitute for obtaining permission. </p>
            <p>The 1961 <em>Report of the Register of Copyrights on the General Revision of the U.S. Copyright Law</em> cites examples of activities that courts have regarded as fair use: &ldquo;quotation of excerpts in a review or criticism for purposes of illustration or comment; quotation of short passages in a scholarly or technical work, for illustration or clarification of the author&rsquo;s observations; use in a parody of some of the content of the work parodied; summary of an address or article, with brief quotations, in a news report; reproduction by a library of a portion of a work to replace part of a damaged copy; reproduction by a teacher or student of a small part of a work to illustrate a lesson; reproduction of a work in legislative or judicial proceedings or reports; incidental and fortuitous reproduction, in a newsreel or broadcast, of a work located in the scene of an event being reported.&rdquo; </p>
            <p>Copyright protects the particular way authors have expressed themselves. It does not extend to any ideas, systems, or factual information conveyed in a work. </p>
            <p>The safest course is to get permission from the copyright owner before using  copyrighted material. The Copyright Office cannot give this permission. </p>
            <p>When it is impracticable to obtain permission, you should consider avoiding  the use of copyrighted material unless you are confident that the doctrine of  fair use would apply to the situation. The Copyright Office can neither  determine whether a particular use may be considered fair nor advise on possible  copyright violations. If there is any doubt, it is advisable to consult an  attorney.</p>
            <p>&nbsp;</p>
